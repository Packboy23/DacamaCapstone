    // Set the countdown duration in seconds (1 hour = 60 minutes * 60 seconds)
    var countdownDuration = <?php echo 60 * 60; ?>;

    function updateTimer() {
        var minutes = Math.floor(countdownDuration / 60);
        var seconds = countdownDuration % 60;

        document.getElementById('timer').innerHTML = 'Time remaining: ' + pad(minutes) + ':' + pad(seconds);

        if (countdownDuration > 0) {
            countdownDuration--;
            setTimeout(updateTimer, 1000);
